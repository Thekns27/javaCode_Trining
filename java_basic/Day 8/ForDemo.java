public class ForDemo {
	public static void main(String[] args) {
		
	}
	void showName (int count , String name) {
		for 
		// for Clause
		(
		// Initialization Statement
		int i = 0 
		; 
		// Decision Statement
		count > i 
		;
		// Incremental Statement
		i++
		)
		// For block
		{
			System.out.println(i + " " + name);
		}
	}
}